              <li>- Don't leave the place while cooking. If it is strongly needed, don't forget to turn off the heat.</li>
              <li>- Don't leave the plug stuck in the outlet. In the case of big household appliances such as refrigerator or washing machine, try to keep the outlet clean.</li>
              <li>- Avoid binding the electric cords and try not to be squashed under any object.</li>
