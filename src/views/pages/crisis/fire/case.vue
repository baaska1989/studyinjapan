        <p class="padT15">Please evacuate immediately when the fire is about to reach the ceiling. Please leave quickly without caring your appearance or your belongings. Cover your mouth and nose with a wet towel, drop to the floor and crawl on your knees so that you don't breathe the smoke, and try getting outdoors. In addition, when evacuating, close the door and windows of the room with fire and shut the air, so that the fire doesn't get stronger and also prevent the fire to spread.<br>
          <br>
          Moreover, it is required to check how to evacuate in case of a fire; the location of the emergency stairs and emergency ladders; if there is a balcony, check whether you can move to the neighbor's one easily, and don't put any object that could be an obstacle in case of emergency; whether there are no obstacles in the stairs or the corridors of the apartment or flat. Try to have good maintenance of the exit ways.</p>
