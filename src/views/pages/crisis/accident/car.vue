        <div class="lifeH4 padT15"><h4><a id="2">Auto Insurance</a></h4></div>
        <p class="padT15">All drivers are required by the law to buy "Automobile Damage Compensation Liability Insurance (jibaiseki)" in Japan. Compensation you ought to pay for the person who suffers an injury or death in a road accident can be covered by the insurance.</p>

        <div class="lifeH5 padT15"><h5>Optional Automobile Insurance needed also</h5></div>
        <p class="padT15">Automobile Damage Compensation Liability Insurance compensates for certain damage or injury to the other party and has a maximum limit for it; the maximum of 1.2 million yen for a body injury, 30 million yen for death, and 40 million yen for a injury which has serious aftereffects. When the amount exceeds the insurance coverage, person responsible for the accident has to pay out of his/her own pocket.<br>
          <br>
          Besides, the following items you damaged in an accident are not covered by the compulsory insurance; damage to the vehicle of the person involved, damage to buildings, telegraph poles, crash barriers, etc.<br>
          <br>
          Therefore, it is highly recommended that every driver subscribes to the optional insurance which covers compensation payment exceeding the limit of coverage by the compulsory insurance, as damage compensation in Japan tends to be quite costly in the event of a vehicle accident. If the injured has to miss work after the accident, it might be you who pay compensation for income loss. And it might be you who pay for business suspension benefits if a damage to a vehicle or building causes bad effects on someone's business. Again, the obligatory insurance only compensates for certain damage within certain limits while optional insurance may cover what is exceeded.<br>
          <br>
          Optional insurance is, of course, not mandatory. However, your car should be carried optional insurance as well as obligatory insurance without trying to save money for it.</p>


