        <div class="lifeH5 padT15"><h5>Pay attention to the battery</h5></div>
        <p class="padT10">During a disaster, please pay due attention not to run out of the battery of your mobile phone. Frequently making calls to try to get them through, or leaving your phone on the power even in a place with no signal will consume battery charge more quickly. Battery pack used more than one year will also be consumed quickly. The use of mobile phone should be kept to minimum and cut off at the outside of the service area so that battery charge will last as long as possible.<br>
          <br>
          Mobile companies will offer free service of charging batteries at the time of disasters, but it is recommended to get extra battery packs by yourself and keep in a emergency kit. Better to be prepared with a hand-powered or solar-powered battery charger as well.</p>

