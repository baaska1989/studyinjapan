        <p class="padT15">Communication regulations by mobile phone companies can be expected so as not to get network tied up by being flooded with phone contacts. At the time of Niigata-ken Chuetsu Earthquake which took place in July 2007, the companies of NTTdocomo, and au regulated 80 to 90 percent of communication. This means that the probability of your call establishing a connection is one in five to one in ten. Also, if the power source at wireless base stations fail due to blackout caused a disaster, all the telecommunication service would be cut off.<br>
