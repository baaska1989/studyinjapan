        <p>If we are struck by a large-scale disaster, naturally we would like to inform the family and friends of our safety as early as possible. On the contrary, when the family or friends are within a location of disaster we would be eager to confirm their safety. Hence, everyone uses their phones simultaneously both from outside and inside the affected areas during disasters at once, and it would become difficult to make contact by phones due to phone network congestion. One is likely to make calls many times until they would get through, but please avoid such an action because it makes phone network become beyond capacity to establish any connection.</p>

