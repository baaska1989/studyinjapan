        <p class="padT15">More often than not, they just write down the addresses and maps of the evacuation places on their website or guidebooks they issue, and it is difficult to tell exactly where the places are unless you are very familiar with geographical circumstances and the names of schools and parks in your town. Only by the maps, we expect that you can not imagine accurately how far it is, how wide or narrow the streets are, nor what on earth the evacuation areas is like, especially when you are a foreign resident there.<br>
