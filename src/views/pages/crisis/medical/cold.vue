        <p>We have four seasons in Japan. The winter period of December, January and February, is the coldest among all seasons. Students from countries with hot climate would enjoy the snowfall in winter. Yet, during the winter time, temperature may sink to 10 to 20 degrees below zero in some region, while in summer the temperature gets over 35 degrees Celsius. Students who are not accustomed to cold weather could get sick soon, for not knowing when to start wearing winter clothes, or how to protect against cold.<br>
