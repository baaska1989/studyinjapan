        <div class="lifeH4"><h4><a id="2">Influenza Infection</a></h4></div>
        <p class="padT15">One common infectious disease in winter is the "Influenza ". It is highly contagious which infects from animals to person, from person to person in a grand scale. When catching the infection, it causes you fever of higher than 38 degrees, headaches, pain all over your body, sore throat or snivel. In the most serious cases, it could even lead one to death. Low humidity is the appropriate climate for the influenza virus, which is the reason why it becomes active in the dry winter.</p>

        <div class="lifeH4"><h4><a id="3">Protect yourself against Infection</a></h4></div>
        <p class="padT15">It is said that a person becomes infected through drop infection such as cough or sneeze, or by touching (touching one's nose, mouth or eyes after touching any object on which virus adheres). To protect yourself against infection, it is important to wash your hands and gargle. It is also important to keep one's condition by taking enough nutrition and enough sleep.</p>

