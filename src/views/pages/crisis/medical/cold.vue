        <div class="lifeH4 padT15"><h4><a id="1">Avoid catching a cold</a></h4></div>
        <p class="padT15">In winter, we often see students who are not accustomed to the cold, dressed lightly for not having thick coats or not knowing when to change clothes and get ready for the cold. Even if you think that your will endure a bit of cold weather, your body could be in a vulnerable condition against the cold virus. When you feel a little cold, which is the sign from your body of its vulnerability, you need to start taking measures against the cold.</p>

        <div class="boxed marT10">
          <p class="padT5">1. Temperature and humidity in rooms</p>
          <p class="padT10">When the winter is very cold, you might raise the temperature of your room extremely high with heating apparatus. But it will make your body lose resistance to disease and you could get sick easily. Also from the point of environmental and economical view, it is advisable to set the temperature of the heating apparatus at 20 to 22 degrees Celsius. Moreover, the air gets dry in winter. Because the cold virus gets active in dry condition, try to keep the humidity of your room at 50 to 60 percents by using a humidifier or hanging wet towels in your room.</p>
        </div>
        <div class="boxed marT10">
          <p class="padT5">2. Socks and scarves</p>
          <p class="padT10">To keep your feet and neck warm eases coldness. Put on socks also when you are indoors, and if you still feel cold, scarves or towel around your neck will help.</p>
        </div>
        <div class="boxed marT10">
          <p class="padT5">3. Wash your hands and gargle</p>
          <p class="padT10">Never forget to wash your hands with soap and gargle when you get back from outside. If you have a sore throat, gargle will help you ease the pain.</p>
        </div>
        <div class="boxed marT10">
          <p class="padT5">4. Not to feel chilly after taking a bath</p>
          <p class="padT10">After taking a shower, moisture deprives your body of heat when your wet body becomes dry, which is called a chill after bath. Wipe off your body immediately after a shower and dress appropriately even if you feel warm from taking a bath.<br>
            <br>
