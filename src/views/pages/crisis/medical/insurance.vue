        <div class="lifeH4 padT15"><h4><a id="1">National Health Insurance (NHI)</a></h4></div>

        <p class="padT15">Japan has a compulsory insurance system which all those living in Japan must be covered by some form of public insurance. Foreign students are designed to apply for the "National Health Insurance". This system is administered by the municipalities. One may apply for it as soon as you register as an alien resident, and pay the monthly premium. If you join the NHI, you only need to pay 30% of the medical expenses at the hospital.</p>

        <div class="lifeH5 padT15"><h5>Who can apply for NHI?</h5></div>
        <p class="padT15">Those who aren't covered by any system listed below may apply for NHI.</p>
        <ul class="padT15 padL15">
          <li>1. other Social Insurance</li>
          <li>2. Livelihood protection</li>
          <li>3. Health Insurance Union</li>
          <li>4. Health Care System for the latter-stage elderly people</li>
        </ul>
        <p class="padT15">Foreigners that will live in Japan for 1 year or more must join. It is not possible to pay in full in case of illness, and to save money by not applying for NHI.<br>
          <br>
          If your stay is less than one year, you can't apply for it.</p>


        <div class="lifeH5 padT15"><h5>How to apply?</h5></div>
        <p class="padT15">You need to apply for NHI in 14 days after you register in the municipality you live. To apply, one must first register their residency. Moreover, if you resign your job or when you get a baby, you are required to apply for the NHI. If the application steps were delayed, you will be asked to pay the monthly premium from the day you started to live in that municipality, which means to pay a large amount at once.</p>


        <div class="lifeH5 padT15"><h5>Medical Expenses covered by NHI</h5></div>
