          <li>- cosmetic surgery</li>
          <li>- physical checkup</li>
        </ul>

        <div class="lifeH5 padT15"><h5>Discount in monthly premium</h5></div>
        <p class="padT15">In case of foreign students, it is possible you to get some discounts in monthly premium. For, the insurance premium is calculated on a one-year basis, based on your previous year income in Japan. The calculation method is different in each municipality, so please check the detail in the public office where you live. In addition, to get the discount, you need an additional application. Please don't forget it when you apply for NHI.</p>
