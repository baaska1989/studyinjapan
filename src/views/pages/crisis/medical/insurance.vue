        <p class="padT15">Please keep in mind that the cases listed below will not be covered by NHI.</p>
        <ul class="padT15 padL15">
          <li>- amenity Bed Cost" when you get hospitalized in a private room</li>
          <li>- in case you needed a special treatment or medicine</li>
